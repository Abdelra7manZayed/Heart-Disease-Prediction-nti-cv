import streamlit as st
import pandas as pd
import numpy as np
import joblib
from tensorflow.keras.models import load_model

st.set_page_config(
    page_title="Heart Disease Prediction",
    page_icon="❤️",
    layout="wide"
)

model = load_model("heart.keras")
scaler = joblib.load("scaler.joblib")

st.title("❤️ Heart Disease Prediction using NN")

st.write(
    "Enter the patient's information."
)

age = st.slider("Age",20,90,45)
sex = st.selectbox("Sex",[0,1],format_func=lambda x:"Female" if x==0 else "Male")
cp = st.selectbox("Chest Pain Type",[0,1,2,3])
trestbps = st.slider("Resting Blood Pressure",80,220,120)
chol = st.slider("Cholesterol",100,600,220)
fbs = st.selectbox("Fasting Blood Sugar >120",[0,1])
restecg = st.selectbox("Rest ECG",[0,1,2])
thalach = st.slider("Maximum Heart Rate",60,220,150)
exang = st.selectbox("Exercise Induced Angina",[0,1])
oldpeak = st.slider("Oldpeak",0.0,6.5,1.0)
slope = st.selectbox("Slope",[0,1,2])
ca = st.selectbox("Major Vessels",[0,1,2,3,4])
thal = st.selectbox("Thal",[0,1,2,3])

if st.button("Predict"):

    sample = np.array([[

        age,
        sex,
        cp,
        trestbps,
        chol,
        fbs,
        restecg,
        thalach,
        exang,
        oldpeak,
        slope,
        ca,
        thal

    ]])

    sample = scaler.transform(sample)

    prediction = model.predict(sample)[0][0]

    st.subheader("Prediction")

    st.progress(float(prediction))

    if prediction >= 0.5:
        st.error(f"High Risk ({prediction*100:.2f}%)")
    else:
        st.success(f"Low Risk ({(1-prediction)*100:.2f}%)")

    st.metric("Heart Disease Probability",f"{prediction*100:.2f}%")